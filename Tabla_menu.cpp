        Generador << "INSERT INTO MENU (TIPO, REST_RUC, PRECIO, DISPONIBILIDAD)" << endl;
        Generador << "VALUES ('";
        if (i%3 == 0) Generador << "Económico";
        else if (i%3 == 1) Generador << "Especial";
        else if (i%3 == 2) Generador << "Ejecutivo";
        Generador << "', '" << RUC[i] << "', '";
        if (i%3 == 0) {
            double precio = (8-6)*((double)rand()/(double)RAND_MAX)+6;
            Generador << setprecision(3) << precio;
        }
        else if (i%3 == 1) {
            double precio = (12-9)*((double)rand()/(double)RAND_MAX)+9;
            Generador << setprecision(3) << precio;;
        }
        else if (i%3 == 2) {
            double precio = (15-12)*((double)rand()/(double)RAND_MAX)+12;
            Generador << setprecision(3) << precio;
        }
